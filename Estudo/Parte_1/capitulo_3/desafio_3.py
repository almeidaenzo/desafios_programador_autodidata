#Escreva um programa que exiba uma mensagem se uma variável for menor ou 
# igual a 10, outra mensagem se a variável for maior do que 10, mas menor ou igual a 
# 25, e ainda outra mensagem se a variável for maior do que 25.


var = 10
if var <= 10:
    print("É menor ou igual a 10")
elif var <= 25:
    print("Maior que 10")
else:
    print("Maior que 25")